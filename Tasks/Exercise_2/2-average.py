#!/usr/bin/python3
print("This program calculates the average of three exam scores")
score1 = eval(input("Enter the first exam score: "))
score2 = eval(input("Enter the second exam score: "))
score3 = eval(input("Enter the third exam score: "))
total_score = score1 + score2 + score3
print("The average score is ", total_score / 3)
